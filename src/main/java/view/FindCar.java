    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        //table is not empty
        if (tblCars.getRowCount() > 0) {
            //validate that a record is selected
            if (tblCars.getSelectedRow() !=-1) {
                
                //get id of the element to delete
                int idCar = Integer.parseInt(String.valueOf(tblCars.getValueAt(tblCars.getSelectedRow(), 0)));
                
                controller.deleteCar(idCar);
                showMesage("Car Deleted Successfully", "info", "Successful Removal");
                LoadTable();
                
            }else{
               showMesage("You have not selected a record", "error", "Error when deleting");
            }
            
        }else{
            showMesage("The table is empty, it cannot be deleted", "error", "Error when deleting");
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

