    private void LoadTable() {

        
        // change table to editable
        DefaultTableModel modelTable = new DefaultTableModel(){
            
            @Override
            public boolean isCellEditable(int row , int column){
             return false;
            };
       
    };

        //give column titles
        
        String titles[] ={"Id","Model","Brand","Engine","Color","Patent","Doors"};
        modelTable.setColumnIdentifiers(titles);
        
        //bring data from DB
        
        List <Cars> listCars  = controller.showCars();
        
        // set data of table
        if (listCars != null) {
            
            for (Cars car : listCars) {
                Object[] object = {car.getId(),car.getModel(),car.getBrand(),car.getEngine(),car.getColor(),car.getPatent(),car.getDoors()}; 
               modelTable.addRow(object);
            }
            
        }
        
        tblCars.setModel(modelTable);
        
    }
    
    public void showMesage(String Mensaje, String tipo , String titulo){
    
        JOptionPane optionPane = new JOptionPane(Mensaje);
        if (tipo.equals("info")) {
            optionPane.setMessageType(JOptionPane.INFORMATION_MESSAGE);
        }else if(tipo.equals("error")){
        optionPane.setMessage(JOptionPane.ERROR_MESSAGE);
        }
        JDialog dialog = optionPane.createDialog(titulo);
        dialog.setAlwaysOnTop(true);
        dialog.setVisible(true);
        
    }
